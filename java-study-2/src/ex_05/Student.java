package ex_05;

// 클래스란 ? 객체를 만들기 전 설계도면입니다.
// 컴파일 타임 : 코드를 작성하고 javac 명령어 실행 시점까지
// compile time : 소스 코드를 기계어로 변환하는 과정까지의 시간
// 런타임 : java 명령어를 통해 프로그램이 실행 시점 부터 프로그램 종료 시점 까지
// runtime : 프로그램이 실제 실행되는 동안의 시간
public class Student {

    String name;    // 학생 이름 (문자열)
    int grade;      // 학년
    String major;   // 학과
    double height;  // 학생의 키
    double weight;  // 학생의 몸무게

} // end of class
