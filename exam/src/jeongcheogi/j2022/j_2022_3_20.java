package jeongcheogi.j2022;

// 2022년 3회 기출문제 20번
// 1부터 998까지 숫자 중에서 3의 배수이면서 홀수인 가장 큰 숫자를 찾는 문제
public class j_2022_3_20 {
	public static void main(String[] args) {
		// 조건에 맞는 숫자를 저장할 변수 선언 및 초기화
		int a = 0;
		
		// 1부터 998까지 반복하는 for문
		for(int i = 1; i < 999; i++) {
			// i가 3의 배수이면서 동시에 홀수인지 검사
			// i % 3 == 0 : 3으로 나눈 나머지가 0 (3의 배수)
			// i % 2 != 0 : 2로 나눈 나머지가 0이 아님 (홀수)
			if(i % 3 == 0 && i % 2 != 0) {
				// 조건에 맞는 숫자를 변수 a에 저장
				// 반복하면서 조건에 맞는 마지막 숫자가 최종적으로 저장됨
				a = i;
			}
		}
		System.out.print(a); // 출력 결과 : 993
	}
}
